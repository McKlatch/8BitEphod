<script>
import { eventBus } from '../../main'

export default {
  props: ['gems'],
  data () {
    return {
      modalActive: false
    }
  },
  methods: {
    flash (e) {
      const shapes = [...e.target.children]
      shapes.forEach(shape => {
        let rgb = shape.style.fill.match(/\d+/g)
        if (rgb !== null) {
          rgb = rgb.map(num => parseInt(num) + 18)
          shape.style.fill = `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`
        }
      })
    },
    unflash (e) {
      const shapes = [...e.target.children]
      shapes.forEach(shape => {
        let rgb = shape.style.fill.match(/\d+/g)
        if (rgb !== null) {
          rgb = rgb.map(num => parseInt(num) - 18)
          shape.style.fill = `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`
        }
      })
    },
    show (modal) {
      if (modal === '^ ^ 8' || modal === 'Bit' || modal === 'Ephod') return
      this.$router.push(modal)
      this.$modal.show(modal.toLowerCase())
    },
    urlPush (url) {
      window.location.href = url
    }
  },
  created () {
    eventBus.$on('modalState', (state) => {
      this.modalActive = state
    })
  },
  mounted () {
    // make interactive gems show pointer cursor
    const gems = JSON.parse(JSON.stringify(this.gems))
    for (let key in gems) {
      if (gems[key]) {
        const shape = document.querySelector(`#${key}`)
        if (!shape.textContent.includes('8') && !shape.textContent.includes('Bit') && !shape.textContent.includes('Ephod')) {
          shape.classList.add('clickable')
        }
      }
    }
  }
}
